#!/usr/bin/env node
/**
 * Generate src/module/types/i18n-keys.d.ts as a string-literal union of every
 * dotted key path in src/lang/en.json. The typed `t(key)` helper in
 * src/module/i18n/t.ts uses this union to reject unknown keys at compile time —
 * making the CLAUDE.md "all player-facing strings live in the langpack" rule
 * mechanically enforceable.
 *
 * The langpack mixes structural keys (e.g. "TYPES.Actor.character") with
 * already-leaf strings; we only emit keys whose terminal value is a string.
 *
 * Run automatically pre-commit (registered in .husky/pre-commit). Idempotent —
 * re-running on unchanged input produces unchanged output.
 *
 * Usage:
 *   node scripts/gen-i18n-types.mjs            # regenerate
 *   node scripts/gen-i18n-types.mjs --check    # exit non-zero if stale
 */
import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { resolve } from 'node:path';

const LANG = resolve(process.cwd(), 'src/lang/en.json');
const OUT = resolve(process.cwd(), 'src/module/types/i18n-keys.d.ts');
const checkMode = process.argv.includes('--check');

function flatten(obj, prefix, into) {
    for (const k of Object.keys(obj)) {
        const v = obj[k];
        const key = prefix ? `${prefix}.${k}` : k;
        if (v && typeof v === 'object' && !Array.isArray(v)) {
            flatten(v, key, into);
        } else if (typeof v === 'string') {
            into.add(key);
        }
    }
}

const lang = JSON.parse(readFileSync(LANG, 'utf8'));
const keys = new Set();
flatten(lang, '', keys);
const sorted = [...keys].sort();

const banner =
    `// AUTO-GENERATED by scripts/gen-i18n-types.mjs from src/lang/en.json. Do not edit by hand.\n` +
    `// Run \`pnpm i18n:gen\` (or commit; the pre-commit hook regenerates) to refresh.\n` +
    `\n`;
const body =
    `export type I18nKey =\n` +
    sorted.map((k) => `    | '${k.replace(/'/g, "\\'")}'`).join('\n') +
    `;\n`;

const serialized = banner + body;

if (checkMode) {
    if (!existsSync(OUT)) {
        console.error('[gen-i18n-types] missing — run: pnpm i18n:gen');
        process.exit(1);
    }
    const onDisk = readFileSync(OUT, 'utf8');
    if (onDisk !== serialized) {
        console.error('[gen-i18n-types] stale — run: pnpm i18n:gen');
        process.exit(1);
    }
    console.log(`[gen-i18n-types] OK (${sorted.length} keys)`);
    process.exit(0);
}

writeFileSync(OUT, serialized, 'utf8');
console.log(`[gen-i18n-types] wrote ${OUT}: ${sorted.length} keys.`);
